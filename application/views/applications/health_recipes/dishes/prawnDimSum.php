            <h3>Ingredients</h3>
            450g/1lb chicken 
            <br>breasts, no skin or bone - cut into strips 
            <br>1 egg white 
            <br>2 tsp cornflour 
            <br>good pinch salt 
            <br>sesame oil 
            <br>250ml/8fl oz groundnut oil 
            <br>3 lemons, juice and zest 
            <br>85ml/2fl oz chicken stock 
            <br>1 tbsp sugar 
            <br>1 tbsp light soy sauce 
            <br>splash of dry sherry 
            <br>1 clove garlic, crushed 
            <br>1 red chilli, finely chopped 
            <br>1 tsp cornflour or arrowroot - mixed with water 
            <br>small bunch spring onions, shredded 
            <br>seasoning 
        </div>
        <div class="col-md-8">

            <h3>Preparation method</h3>
            <ol>
                <li>
                    <p>Combine the egg white, cornflour, salt, a little sesame oil and chicken in a bowl - stir to make sure the chicken is well coated. Leave for about 20 minutes.</p>
                </li>
                <li>
                    <p>Heat a wok until very hot, add some groundnut oil and heat until smoking. Remove from the heat and add the chicken - stir quickly to stop it from sticking and cook until the chicken turns white. Drain the chicken over a heatproof bowl, set the chicken to one side, allow the oil to cool and discard.</p>
                </li>
                <li>
                    <p>Wipe the wok clean and make the sauce. Add the stock, lemon juice and zest, sugar, soy and dry sherry together with the garlic and chilli. Bring to the boil and whisk in the cornflour or arrowroot mixture. Simmer gently for a few moments and then add the chicken. Stir fry to make sure the chicken is well coated and heated through properly.</p>
                </li>
                <li>
                    <p>Add a little sesame oil as final seasoning and serve scattered with spring onions.</p>
                </li>
            </ol>
